const Source = enum {
    input_properties,
    event,
    sync,
    keyboard,
    pointer,
    relative,
    absolute,
    @"switch",
    misc,
    led,
    repeat,
    sound,
};

/// For logging purposes
pub fn linuxInputEventCodeToString(source: Source, code: u32) ?[]const u8 {
    return switch (source) {
        .input_properties => return inputProperties(code),
        .event => return event(code),
        .sync => return sync(code),
        .keyboard => return keyboard(code),
        .pointer => return pointer(code),
        .relative => return relative(code),
        .absolute => return absolute(code),
        .@"switch" => return @"switch"(code),
        .misc => return misc(code),
        .led => return led(code),
        .repeat => return repeat(code),
        .sound => return sound(code),
    };
}

